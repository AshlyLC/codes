numero1 = int(input("Escribe un número entero: "))
numero2 = int(input("Escribe otro número entero: "))

if numero1 > numero2:
    print("El primer número es tremendamente más grande que el segundo.")
elif numero1 < numero2:
    print("El primer número es microscópico comparado con el segundo.")
else:
    print("¡Son iguales! Ambos números son tocayos.")